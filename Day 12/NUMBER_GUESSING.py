def attempting(sec_number, _attempts, _allowed_attempts):
    while _allowed_attempts > 0:
        print(f"You have {_allowed_attempts} attempts remaining to guess the number.")
        guess = int(input("Make a guess:"))
        if guess == sec_number:
            print("Correct!")
            print("You took " + str(_attempts) + " attempts.")
            exit()
        elif guess > sec_number:
            print("Too high!")
            _allowed_attempts -= 1
            _attempts += 1
        elif guess < sec_number:
            print("Too low!")
            _allowed_attempts -= 1
            _attempts += 1
    print("You ran out of attempts. The number was " + str(sec_number) + ".")


